<script lang="ts" setup>
import { User, Lock } from '@element-plus/icons-vue';
import { reactive, ref } from 'vue';
import useUserStore from '@/store/modules/user';
import { useRouter, useRoute, LocationQueryValue } from 'vue-router';
import { ElNotification, FormInstance } from 'element-plus';
import { getTime } from '@/utils/time';
// import SvgIcon from '@/components/SvgIcon/index.vue';

const userStore = useUserStore();
const $router = useRouter();
const $route = useRoute();

const fromData = reactive({
	username: 'admin',
	password: '123456',
});

const ruleFormRef = ref<FormInstance>();
const isrememberMe = ref<boolean>(true); // 记住我
const loading = ref(false);

const login = async () => {
	await ruleFormRef.value?.validate();

	loading.value = true;
	try {
		const result = await userStore.userLogin(fromData);
		console.log(result);
		const prevRoutePath = ($route.query.redirect as LocationQueryValue) || '/';
		$router.push({ path: prevRoutePath });
		ElNotification({
			type: 'success',
			message: '登录成功',
			title: `Hi, ${getTime()}好`,
		});
	} catch (error: any) {
		ElNotification({
			type: 'error',
			message: error.message,
		});
	}
	loading.value = false;
};

const usernameValidate = (rule: any, value: any, callback: any) => {
	if (value === '') {
		callback(new Error('用户名不能为空'));
	} else if (value.length < 4) {
		callback(new Error('账号长度不能小于4'));
	}
	callback();
};

const passwordValidate = (rule: any, value: any, callback: any) => {
	if (value === '') {
		callback(new Error('密码不能为空'));
	} else if (value.length < 6) {
		callback(new Error('密码长度不能小于 6位'));
	}
	callback();
};

const rules = {
	username: [{ validator: usernameValidate, trigger: 'change' }],
	password: [{ validator: passwordValidate, trigger: 'change' }],
};
